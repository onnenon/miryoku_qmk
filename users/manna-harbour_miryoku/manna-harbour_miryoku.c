void render_status(void) {
// Host Keyboard Layer Status
    switch (get_highest_layer(layer_state)) {
        case U_BASE:
            oled_write_raw_P(display, sizeof(display));
            break;
        case U_EXTRA:
            oled_write_raw_P(extra, sizeof(extra));
            break;
        case U_TAP:
            oled_write_raw_P(tap, sizeof(tap));
            break;
        case U_BUTTON:
            oled_write_raw_P(button, sizeof(button));
            break;
        case U_NAV:
            oled_write_raw_P(navi, sizeof(navi));
            break;
        case U_MOUSE:
            oled_write_raw_P(mouse, sizeof(mouse));
            break;
        case U_MEDIA:
            oled_write_raw_P(media, sizeof(media));
            break;
        case U_NUM:
            oled_write_raw_P(number, sizeof(number));
            break;
        case U_SYM:
            oled_write_raw_P(symbol, sizeof(symbol));
            break;
        case U_FUN:
            oled_write_raw_P(function, sizeof(function));
            break;
        case U_GAME:
            oled_write_raw_P(gaming, sizeof(gaming));
            break;
        default:
            // Or use the write_ln shortcut over adding '\n' to the end of your string
            oled_write_ln_P(PSTR("Undefined"), false);
    }
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (!is_keyboard_master()) {
        return OLED_ROTATION_180;  // flips the display 180 degrees if offhand
    }

    return rotation;
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_status();  // Renders the current keyboard state (layer, lock, caps, scroll, etc)
    } else {
        render_logo_user();  // Renders a static logo
        oled_scroll_left();  // Turns on scrolling
    }
    return false;
}
